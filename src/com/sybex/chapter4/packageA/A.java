package com.sybex.chapter4.packageA;

public class A {

  public void publicMethod() {
    System.out.println("Public method in package A.");
  }
  
  private void privateMethod() {
    System.out.println("Private method in package A.");
  }
  
  protected void protectedMethod() {
    System.out.println("Protected method in package A.");
  }
  
  void defaultMethod() {
    System.out.println("Default method in package A.");
  }
  
}
